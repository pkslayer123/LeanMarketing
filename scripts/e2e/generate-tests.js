// Helper: generate a spec file for a feature (template or Claude)
// ---------------------------------------------------------------------------
function generateSpec(featureKey) {
  if (CLAUDE_MODE) {
    const feature = manifest.features[featureKey];
    if (!feature) {
      return generateSpecContent(featureKey);
    }

    if (!isClaudeAvailable()) {
      console.log(
        `  Claude CLI not available. Falling back to template for ${featureKey}.`
      );
      return generateSpecContent(featureKey);
    }

    console.log(
      `Generating source-aware test for ${featureKey} via Claude CLI...`
    );
    const claudeOutput = generateWithClaude(featureKey, feature);
    if (claudeOutput) {
      console.log(`  Source-aware test generated successfully for ${featureKey}.`);
      return claudeOutput;
    }
    console.log(`  Falling back to template for ${featureKey}.`);
  }

  return generateSpecContent(featureKey);
}

