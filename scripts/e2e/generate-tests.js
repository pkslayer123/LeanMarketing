/**
 * Check if Claude CLI is available.
 */
function isClaudeAvailable() {
  try {
    execSync("claude --version", { stdio: "pipe", timeout: 5000 });
    return true;
  } catch {
    return false;
  }
}

/**
 * Read first N lines of a file (or all if fewer lines).
 */
function readFirstLines(filePath, maxLines) {
  try {
    const content = fs.readFileSync(filePath, "utf-8");
    const lines = content.split("\n");
    return lines.slice(0, maxLines).join("\n");
  } catch {
    return null;
  }
}

/**
 * Resolve page paths from manifest to actual source files in the app/ directory.
 * e.g., "/admin/people" -> "app/admin/people/page.tsx"
 */
function resolvePageSourceFiles(pages) {
  const sources = [];
  for (const pagePath of pages) {
    // Skip wildcard pages like /mocs/*/hotspots
    if (pagePath.includes("*")) {
      continue;
    }

    // Convert URL path to file path: /admin/people -> app/admin/people/page.tsx
    const cleaned = pagePath.replace(/^\//, "");
    const candidates = [
      path.join(ROOT, "app", cleaned, "page.tsx"),
      path.join(ROOT, "app", cleaned, "page.ts"),
      // Dynamic routes: /mocs/[id] -> app/mocs/[id]/page.tsx
      path.join(ROOT, "app", cleaned.replace(/\/[^/]+$/, "/[id]"), "page.tsx"),
    ];

    for (const candidate of candidates) {
      if (fs.existsSync(candidate)) {
        const relativePath = path.relative(ROOT, candidate).replace(/\\/g, "/");
        const content = readFirstLines(candidate, 300);
        if (content) {
          sources.push({ path: relativePath, content });
        }
        break;
      }
    }
  }
  return sources;
}

/**
 * Find the persona constant names from personas.ts for a list of persona IDs.
 * e.g., "cliff-patience" -> "CLIFF_PATIENCE"
 *
 * Uses line-based search instead of regex to avoid Node.js string escaping issues.
 */
function resolvePersonaConstants(personaIds) {
  try {
    const lines = fs.readFileSync(PERSONAS_FILE, "utf-8").split("\n");
    const results = [];
    const idSet = new Set(personaIds);

    // Scan lines: when we find `export const X: Persona`, remember the const name.
    // When we find `id: "some-id"` within the next few lines, map it.
    let lastConstName = null;
    let lastConstLine = -1;

    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];

      // Match: export const CLIFF_PATIENCE: Persona = {
      const constMatch = line.match(/export\s+const\s+(\w+)\s*:\s*Persona/);
      if (constMatch) {
        lastConstName = constMatch[1];
        lastConstLine = i;
        continue;
      }

      // Match: id: "cliff-patience",
      if (lastConstName && i - lastConstLine <= 5) {
        const idMatch = line.match(/id:\s*"([^"]+)"/);
        if (idMatch && idSet.has(idMatch[1])) {
          results.push({ id: idMatch[1], constName: lastConstName });
          idSet.delete(idMatch[1]);
          lastConstName = null;
        }
      }
    }
    return results;
  } catch {
    return [];
  }
}

/**
 * Build the Claude prompt for source-aware test generation.
 */
function buildClaudePrompt(featureKey, feature) {
  const pageSourceFiles = resolvePageSourceFiles(feature.pages || []);
  const personaConstants = resolvePersonaConstants(feature.personas || []);

  // Read example spec (first 100 lines)
  const exampleSpec = readFirstLines(EXAMPLE_SPEC, 100) || "(example spec not found)";

  // Build page source section
  let pageSourceSection = "";
  if (pageSourceFiles.length > 0) {
    pageSourceSection = "\n## Actual page source files\n\n";
    for (const src of pageSourceFiles) {
      pageSourceSection += `### ${src.path}\n\`\`\`tsx\n${src.content}\n\`\`\`\n\n`;
    }
  } else {
    pageSourceSection =
      "\n## Page source files\n\nNo directly-mapped page files found. " +
      "Generate tests based on the code areas and permissions listed above.\n\n";
  }

  // Build persona imports section
  let personaImports = "";
  let personaList = "";
  if (personaConstants.length > 0) {
    personaImports = personaConstants.map((p) => p.constName).join(", ");
    personaList = personaConstants
      .map((p) => `- \`${p.constName}\` (id: "${p.id}")`)
      .join("\n");
  }

  const prompt = `You are generating a Playwright E2E test file for the ChangePilot (MOC-AI) application.

## Feature: ${featureKey}

### Manifest entry
\`\`\`json
${JSON.stringify(feature, null, 2)}
\`\`\`

### Persona constants to import
${personaList || "(no matching persona constants found — use persona IDs as reference)"}

### Permissions tested by this feature
${(feature.permissions || []).map((p) => `- \`${p}\``).join("\n")}

### Pages to test
${(feature.pages || []).map((p) => `- ${p}`).join("\n") || "(no pages listed — focus on API/permission tests)"}

### Code areas
${(feature.codeAreas || []).map((p) => `- ${p}`).join("\n") || "(none)"}
${pageSourceSection}
## Example spec file (ace-actionitem.spec.ts, first 100 lines)

\`\`\`typescript
${exampleSpec}
\`\`\`

## Test fixture API reference

The \`sim\` fixture (SimulationManager) provides these key methods:

- \`await sim.loginAsDeveloper()\` — Log in as the pool developer account (call in beforeEach)
- \`await sim.simulateAs(PERSONA)\` — Start simulation as a persona (call in beforeEach after login)
- \`await sim.swapTo(persona)\` — Swap to a different persona role mid-test (lightweight, no re-login)
- \`await sim.goto(path)\` — Navigate to a page path (e.g., "/mocs")
- \`await sim.tryAccessPage(path)\` — Navigate and return { status, redirected } for access checks
- \`await sim.evaluatePage()\` — Run oracle validation on the current page, returns { passed, ... }
- \`await sim.validatePageWithOracle(path)\` — Combined goto + oracle validation
- \`await sim.screenshot(name)\` — Take a named screenshot
- \`sim.reportFinding({ severity, page, description })\` — Report a test finding
- \`sim.recordPass()\` — Record a passing check for green tracker
- \`sim.getPage()\` — Get the underlying Playwright Page object

Severity levels: "bug", "ux", "security", "inconsistency", "suggestion", "product"

## Instructions

Generate a complete TypeScript Playwright test file for the \`${featureKey}\` feature. Follow these rules exactly:

1. **Imports**: Use \`import { test, expect } from "../../fixtures/test";\` and import persona constants from \`"../../fixtures/personas"\`.
${personaImports ? `   Import: \`{ ${personaImports} }\`` : "   (define inline persona objects if no constants are available)"}

2. **Structure**: Use \`test.describe("Feature Name", () => { ... })\` with \`test.beforeEach\` that calls \`sim.loginAsDeveloper()\` and \`sim.simulateAs(PRIMARY_PERSONA)\`.

3. **Page access tests**: For each page in the manifest, test that:
   - Allowed personas can access the page (status < 400, not redirected to /login)
   - Use \`sim.tryAccessPage(path)\` and check \`result.status\` and \`result.redirected\`
   - Take screenshots with \`sim.screenshot()\`

4. **Source-aware UI tests**: If page source is provided above, write tests that check for ACTUAL elements visible in the source code:
   - Look for heading text, button labels, tab names, section headers in the source
   - Use \`page.locator()\` or \`page.getByRole()\` or \`page.getByText()\` to assert they exist
   - Do NOT guess or invent UI elements — only test what appears in the source

5. **Permission boundary tests**: For personas that should NOT have access (lower roles), verify they get denied:
   - Use \`sim.swapTo()\` to change to a restricted persona
   - Verify redirect or 403/401

6. **Error handling**: Wrap oracle/screenshot calls in try/catch or use \`.catch(() => {})\` — these are best-effort.

7. **Findings**: Use \`sim.reportFinding()\` when unexpected access is granted or denied.

8. **Output**: Return ONLY the TypeScript file content. No markdown fences, no explanation — just the raw .ts file content.

Generate the test file now:`;

  return prompt;
}

/**
 * Invoke Claude CLI to generate a source-aware test file.
 * Returns the generated content string, or null on failure.
 */
function generateWithClaude(featureKey, feature) {
  const prompt = buildClaudePrompt(featureKey, feature);

  // Write prompt to temp file to avoid shell escaping issues
  fs.writeFileSync(PROMPT_FILE, prompt);

  const model = process.env.TESTGEN_MODEL || "haiku";
  const budget = process.env.TESTGEN_BUDGET || "0.20";

  try {
    const result = execSync(
      `claude --print --dangerously-skip-permissions --model ${model} --max-budget-usd ${budget} < "${PROMPT_FILE}"`,
      {
        cwd: ROOT,
        stdio: ["pipe", "pipe", "pipe"],
        timeout: 120000, // 2 minutes per generation
        env: {
          ...process.env,
          CLAUDE_CODE_ENTRYPOINT: "generate-tests",
          // Unset nesting guard so Claude CLI can launch from within a Claude Code session
          CLAUDECODE: "",
          CLAUDE_CODE: "",
        },
      }
    );

    // Clean up prompt file
    try {
      fs.unlinkSync(PROMPT_FILE);
    } catch {
      /* ignore */
    }

    let output = result.toString().trim();

    // Token accounting
    try {
      const _tl = require("./lib/token-logger");
      const _modelUsed = process.env.TESTGEN_MODEL || "haiku";
      const _inEst = Math.ceil((fs.existsSync(PROMPT_FILE) ? fs.statSync(PROMPT_FILE).size : 0) / 4);
      const _outEst = Math.ceil(output.length / 4);
      _tl.logTokenUsage({ component: "generate-tests", inputTokens: _inEst, outputTokens: _outEst, provider: "claude", model: _modelUsed });
    } catch { /* non-fatal */ }

    // Strip markdown fences if Claude wrapped the output
    if (output.startsWith("```typescript")) {
      output = output.replace(/^```typescript\s*\n/, "").replace(/\n```\s*$/, "");
    } else if (output.startsWith("```ts")) {
      output = output.replace(/^```ts\s*\n/, "").replace(/\n```\s*$/, "");
    } else if (output.startsWith("```")) {
      output = output.replace(/^```\s*\n/, "").replace(/\n```\s*$/, "");
    }

    // Basic validation: must contain fixture import and at least one test
    if (
      !output.includes('from "../../fixtures/test"') ||
      !output.includes("test(") ||
      !output.includes("test.describe(")
    ) {
      console.error(
        `  Claude output failed validation (missing fixture import or test blocks). Falling back to template.`
      );
      return null;
    }

    return output + "\n";
  } catch (err) {
    const stderr = (err.stderr || "").toString().slice(-300);
    const stdout = (err.stdout || "").toString().slice(-300);
    console.error(`  Claude CLI failed: ${stderr || stdout || err.message}`);

    // Clean up prompt file
    try {
      fs.unlinkSync(PROMPT_FILE);
    } catch {
      /* ignore */
    }

    return null;
  }
}

