// --auto: Generate tests for ALL features missing test files
// ---------------------------------------------------------------------------
if (hasFlag("--auto")) {
  fs.mkdirSync(TESTS_DIR, { recursive: true });

  const results = {
    scanned: 0,
    created: [],
    createdWithClaude: [],
    alreadyExist: [],
    claudeFallbacks: [],
  };

  for (const featureKey of Object.keys(manifest.features)) {
    results.scanned++;
    const slug = featureKey.replace(/_/g, "-");
    const specPath = path.join(TESTS_DIR, `${slug}.spec.ts`);

    if (fs.existsSync(specPath)) {
      results.alreadyExist.push(featureKey);
      continue;
    }

    const content = generateSpec(featureKey);
    fs.writeFileSync(specPath, content);
    results.created.push(featureKey);

    // Track whether Claude was used or template fallback
    if (CLAUDE_MODE && content !== generateSpecContent(featureKey)) {
      results.createdWithClaude.push(featureKey);
    } else if (CLAUDE_MODE) {
      results.claudeFallbacks.push(featureKey);
    }
  }

  if (JSON_MODE) {
    console.log(JSON.stringify(results, null, 2));
  } else {
    console.log(`=== Auto-Generate Feature Tests ===\n`);
    console.log(`Scanned: ${results.scanned} features in manifest`);
    console.log(`Already exist: ${results.alreadyExist.length}`);
    console.log(`Created: ${results.created.length}`);
    if (CLAUDE_MODE) {
      console.log(
        `  - via Claude CLI: ${results.createdWithClaude.length}`
      );
      console.log(
        `  - template fallback: ${results.claudeFallbacks.length}`
      );
    }
    if (results.created.length > 0) {
      console.log(`\nNew test files:`);
      for (const f of results.created) {
        const slug = f.replace(/_/g, "-");
        const suffix = results.createdWithClaude.includes(f)
          ? " (claude)"
          : "";
        console.log(`  + e2e/tests/features/${slug}.spec.ts${suffix}`);
      }
    }
    console.log(`\nRun: cd e2e && npm run test:feature-tests`);
  }
  process.exit(0);
}

